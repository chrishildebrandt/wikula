/**
 * initialise block
 *
 * @author       Mateo Tibaquirá
 */
function wikula_wkrecentchangesblock_init()
{
    // Security
    SecurityUtil::registerPermissionSchema('wikula:wkrecentchangesblock:', 'Block ID::');
}

/**
 * get information on block
 *
 * @author       Mateo Tibaquirá
 * @return       array       The block information
 */
function wikula_wkrecentchangesblock_info()
{
    return array('text_type'      => 'recentchanges',
                 'module'         => 'wikula',
                 'text_type_long' => 'Show the wikula recent changes',
                 'allow_multiple' => false,
                 'form_content'   => false,
                 'form_refresh'   => false,
                 'show_preview'   => true);
}

/**
 * display block
 *
 * @author       Mateo Tibaquirá
 * @param        array       $blockinfo     a blockinfo structure
 * @return       output      the rendered bock
 */
function wikula_wkrecentchangesblock_display($blockinfo)
{
    $dom = ZLanguage::getModuleDomain('wikula');
    if (!SecurityUtil::checkPermission('wikula:wkrecentchangesblock', $blockinfo['bid'].'::', ACCESS_READ)) {
        return;
    }

    // Get variables from content block
    $vars = pnBlockVarsFromContent($blockinfo['content']);

    // Defaults
    if (empty($vars['numitems'])) {
        $vars['numitems'] = 5;
    }

    // Check if the wikula module is available.
    if (!pnModAvailable('wikula')) return false;

    $pages = pnModAPIFunc('wikula', 'user', 'LoadRecentlyChanged',
                          array('numitems' => $vars['numitems']));

    if (!$pages) {
        return __('There are no recent changes', $dom);
    }

    $curday = '';
    $pagelist = array();
    foreach ($pages as $page)
    {
        list($day, $time) = explode(' ', $page['time']);
        if ($day != $curday) {
            $dateformatted = date(__('D, d M Y', $dom), strtotime($day));
            $curday = $day;
        }

        if ($page['user'] == pnConfigGetVar('anonymous')) {
            $page['user'] .= ' ('.__('anonymous user', $dom).')'; // anonymous user
        }

        $pagelist[$dateformatted][] = $page;
    }
    unset($pages);

    $render = pnRender::getInstance('wikula', false);
    $render->assign('pagelist', $pagelist);

    // Populate block info and pass to theme
    $blockinfo['content'] = $render->fetch('wikula_block_wkrecentchanges.tpl');
    return themesideblock($blockinfo);
}


/**
 * modify block settings
 *
 * @author       Mateo Tibaquirá
 * @param        array       $blockinfo     a blockinfo structure
 * @return       output      the bock form
 */
function wikula_wkrecentchangesblock_modify($blockinfo)
{
    $dom = ZLanguage::getModuleDomain('wikula');
    // Get current content
    $vars = pnBlockVarsFromContent($blockinfo['content']);

    // Defaults
    if (empty($vars['numitems'])) {
        $vars['numitems'] = 5;
    }

    // Create output object
	// As Admin output changes often, we do not want caching.
    $render = pnRender::getInstance('wikula', false);

    // assign the approriate values
	$render->assign('blockvars', $vars);

    // Return the output that has been generated by this function
	return $render->fetch('wikula_block_wkrecentchanges_modify.tpl');
}


/**
 * update block settings
 *
 * @author       Mateo Tibaquirá
 * @param        array       $blockinfo     a blockinfo structure
 * @return       $blockinfo  the modified blockinfo structure
 */
function wikula_wkrecentchangesblock_update($blockinfo)
{
    $dom = ZLanguage::getModuleDomain('wikula');
    // Get current content
    $vars = pnBlockVarsFromContent($blockinfo['content']);

	// alter the corresponding variable
    $vars['numitems'] = FormUtil::getPassedValue('numitems', 5);

	// write back the new contents
    $blockinfo['content'] = pnBlockVarsToContent($vars);

	// clear the block cache
	$render = pnRender::getInstance('wikula');
	$render->clear_cache('wikula_block_wkrecentchanges.tpl');

    return $blockinfo;
}
